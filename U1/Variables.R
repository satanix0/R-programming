# Integer --> L suffix is required to make a nemuric, to integer.
x <- 2L
print(typeof(x))
print(x)

# Double
y <- 2.547
print(typeof(y))
print(y)

# Complex Numbers
z <- 3 + 2i
print(typeof(z))
print(z)

# Character
a <- "C"
print(typeof(a))
print(a)

# String
b <- "CHARACTER"
print(typeof(b))
print(b)

# Logical
l <- FALSE
print(typeof(l))
print(l)
print(isTRUE(l))
